package labs.lab_2;


public class currency {
    public double dollar_to_inr(double amount){
        return amount* 83.73;
    }
    public double euro_to_inr(double amount){
        return amount * 90.79;
    }
    public double yen_to_inr(double amount){
        return amount *0.54;
    }
    public double inr_to_dollar(double amount){
        return amount* 0.012;
    }
    public double inr_to_euro(double amount){
        return amount * 0.011;
    }
    public double inr_to_yen(double amount){
        return amount *1.84;
    }
}
