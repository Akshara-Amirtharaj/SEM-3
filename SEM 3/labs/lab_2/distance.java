package labs.lab_2;

public class distance {
    public double meter_km(double distance){
        return distance*1000;
    }
    public double miles_km(double distance){
        return distance*1.6;
    }
    public double km_meter(double distance){
        return distance*0.001;
    }
    public double km_miles(double distance){
        return distance*0.62;
    }
}
